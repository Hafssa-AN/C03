int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	k;

	k = 0; 
	while ((s1[k] != '\0' || s2[k] != '\0') && k < n)
	{
		if (s1[k] < s2[k])
		{
			return (-1);
		}
		else if (s1[k] > s2[k])
		{
			return (1);
		}
		k++;
	}
	return (0);
}
